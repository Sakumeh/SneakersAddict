            <p class="article_view_content">
                Après vous avoir présenté la dernière Air Jordan 1 « Court Purple » qui devrait sortir d’ici peu, Jordan Brand vient de dévoiler une nouvelle édition haute en couleurs. 
            Les équipes de la marque au Jumpman multiplient les déclinaisons de ce modèle plus que mythique, rendu célèbre par la légende du basketball Michael Jordan. <br>
			Plus de 35 ans après sa première apparition, la silhouette a su traverser les époques et attire toujours autant les fans du modèle. <br><br>
			Pour la collection Summer 2020, l’Air Jordan 1 High sera déclinée dans un imprimé « Tie Dye » très original.
            La tendance du « Tie Dye » a explosé ces derniers mois et nous la retrouvons sur de nombreux vêtements et depuis peu sur des paires de sneakers. 
            <br><br>La technique consiste à décolorer un textile à la suite d’un nouage en associant et mélangeant différentes couleurs. Cette pratique avait été plébiscitée par le mouvement hippie durant les années 1960 et 1970 et revient actuellement en grande pompe. 
			<br><br>Les clichés de la sneaker montrent en effet un nouveau traitement appliqué sur l’empeigne avec ce dégradé de bleus allant du bleu foncé au bleu turquoise.
			Les teintes se marient parfaitement avec le reste de la tige qui est proposée en blanc et noir.
