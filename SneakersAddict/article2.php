            <h2 class="article_view_title">Air Jordan 4 “White/Metallic Red”</h2>
            <h3 class="article_view_subtitle">Une nouvelle Air Jordan 4 s’inspire de deux anciens modèles de la marque au Jumpman</h3>
            <h4 class="article_view_author"> Auteur : EL ASHRY Sameh, Le 16 Mai 2020 </h4>
            <p class="article_view_content">
				Un vent de nostalgie souffle du côté de chez Jordan Brand qui propose des éditions à l’esprit retro de l’Air Jordan 4 dernièrement. 
				<br>Après l’arrivée du modèle en hommage au personnage de Buggin’ Out dans le film Do The Right Thing, une nouvelle mouture « White/Metallic Red » va débarquer et reprend notamment la teinte rouge de l’Air Jordan 1 OG « Metallic Red ».
				<br><br>La paire revisite également le modèle d’Air Jordan 4 « Classic Green » sorti en 2004 et devenu depuis un classique en la matière.

				<br>La chaussure est proposée dans un code couleur monochrome blanc en cuir sublimé par l’ajout de détails colorés qui créent un look unique en toute simplicité. 
				Contrairement au modèle d’inspiration qui était vert, le choix des designers s’est porté sur une teinte rouge « Metallic Red » dérivée du coloris de l’Air Jordan 1 OG sortie en 1985. Sa particularité est d’avoir un traitement métallisé comme le nom l’indique. 
				
			</p>
			<img src='img/air_jordan_4_metallic.jpg' style="width: 100%;">
			<p class="article_view_content">
			
				Ce coloris est appliqué sur le logo Jumpman sur la languette ainsi qu’au talon mais également sur les passants des lacets et enfin le revers de la languette sur laquelle se dévoile l’inscription « Air Jordan ». 
				<br>Les semelles de propreté affichent aussi cette teinte spécifique. 
				<br>Le reste de la sneaker est propre à l’ADN du modèle sorti en 1989 dessiné par Tinker Hatfield. La bulle d’air apparente est toujours de la partie, tout comme la coupe semi-montante de la paire ou encore sa grille latérale qui lui offre un look unique.
				
				L’Air Jordan 4 White Metallic Red sera commercialisée le 20 juin au prix de 190€. 
				La paire sera notamment proposée sur notre boutique en ligne.
            
